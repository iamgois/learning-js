/* DECLARATIVA */

function funcao() {
    console.log('Thiago Gois')
}

funcao();

/* a função com variável não precisa de uma denominação para poder funcionar */

var dale = function() {
    console.log('Thiago Gois')
};

dale();

/* ARROW FUNCTION, mais simples para diminuir o codigo */ 

var gois = () => {
    console.log('Sou um arrow function!')
}

gois();

